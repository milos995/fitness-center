    private void srediTabelu(String filter) {
        ObjekatZahtev zahtev = new ObjekatZahtev(Operacije.PRETRAZI_KORISNIKE, filter);
        Komunikacija.getInstance().posaljiZahtev(zahtev);
        ObjekatOdgovor odgovor = Komunikacija.getInstance().primiOdgovor();
        if(odgovor.getStatus() == Status.ERROR){
            JOptionPane.showMessageDialog(this, "Sistem ne mo≈æe da naƒëe korisnike po zadatoj vrednosti");
            System.out.println(odgovor.getPoruka());
            return;
        }  
        List<Korisnik> lk = (List<Korisnik>) odgovor.getOdgovor();

        TableModelKorisnici tmk = new TableModelKorisnici(lk);
        jTableKorisnici.setModel(tmk);
    }
